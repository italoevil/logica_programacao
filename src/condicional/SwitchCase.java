package condicional;

public class SwitchCase {

	public static void main(String[] args) {

		//SwitchCase
		
		int numero = 5;
		
		switch(numero) {
		case 1:
			System.out.println("1");
			break;
		case 5:
			System.out.println("5");
			break;
		case 10:
			System.out.println("10");
			break;
		default:
			System.out.println("Sem retorno!");
		}
		
	}

}
