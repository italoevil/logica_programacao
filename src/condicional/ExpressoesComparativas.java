package condicional;

public class ExpressoesComparativas {

	public static void main(String[] args) {
		
		//ExpressoesComparativas < <= == > >= !=
		
		int numero = 10;
		
		//< menor
		System.out.println(numero < 5);
		//<= menor igual
		System.out.println(numero <= 5);
		//> maior
		System.out.println(numero > 5);
		//>= maior igual
		System.out.println(numero >= 5);
		//!= diferente
		System.out.println(numero != 5);
		
	}
}
