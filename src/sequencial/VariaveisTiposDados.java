package sequencial;

public class VariaveisTiposDados {

	public static void main(String[] args) {

		//VARIAVEIS
		int idade = 15; //ideda de 15 anos, sendo guardado na variavel, para usar quando quiser
		
		//EXIBIR A IDADE
		System.out.println(idade);
		
		//CONCATENANDO A IDADE COM STRING
		
		System.out.println(idade + " Anos");
		
		//SOMANDO MAIS UM ANO Á IDADE
		
		int novaIdade = idade + 1;
		System.out.println(novaIdade); 
		
	}
	
}
