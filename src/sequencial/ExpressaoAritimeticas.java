package sequencial;

public class ExpressaoAritimeticas {

	public static void main(String[] args) {

		//ExpressaoAritimeticas  + - * / %
		
		// + adição
		int numero = 0;
		numero =  numero + 10;
		
		// + subtração
		numero = numero - 3;
		
		// * multiplicação
		numero = numero * 2;
		
		// * divisão
		numero = numero / 2;
		
		// % resto da divisão
		numero = numero % 2; 
		System.out.println(numero);
	}
}
